#include "libft.h"

void ft_putendl_fd(char const *s, int fd)
{
	int j;

	j = 0;

	if (!s)
		return ;
	while (s[j] != '\0')
	{
		write(fd, &s[j++], 1);
	}
	write(fd, "\n", 1);
}
